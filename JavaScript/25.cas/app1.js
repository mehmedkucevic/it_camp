// number PROPERTIES - konstante //

// sledecim konstantama mozemo prisupiti jednio pomocu Number:
// Number.Property

// Napomena
// konstante ne mogu biti primenjene na varijable

// 1. MAX_VALUE - vraca najveci broj u JavaScriptu

const najveci = Number.MAX_VALUE
console.log(najveci)

// 2. MIN_VALUE - vraca najmanjibroj u JavaScriptu

const najmanji = Number.MIN_VALUE
console.log(najmanji)

// 3. POSITIVE_INFINITY - vraca infinity

const inf = Number.POSITIVE_INFINITY
console.log(inf)

// 4. NEGATIVE_INFINITY - vraca negativni infinity

const inf2 = Number.NEGATIVE_INFINITY
console.log(inf2)

// 5. NaN - vraca NaN

const notANumber = Number.NaN
console.log(notANumber)